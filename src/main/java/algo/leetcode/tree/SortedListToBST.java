/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */

import algo.dataStructure.ListNode;
import algo.dataStructure.TreeNode;

import java.util.ArrayList;
import java.util.List;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
class SortedListToBST {
    public TreeNode sortedListToBST(ListNode head) {
        List<Integer> list = new ArrayList<>();
        while (head != null) {
            list.add(head.val);
            head = head.next;
        }
        return buildTreeNode(list);
    }
    private TreeNode buildTreeNode(List<Integer> list) {
        if (list.isEmpty()) {
            return null;
        }

        int rootIndex = list.size()/2;
        TreeNode root = new TreeNode(list.get(rootIndex));
        root.left = buildTreeNode(list.subList(0, rootIndex));
        root.right = buildTreeNode(list.subList(rootIndex+1, list.size()));
        return root;
    }
}
